function sendRequest(Method, URL = "", Headers = {}) {
    if (!Method) return console.warn("Method is not defined");

    return new Promise((res, rej) => {
        fetch(URL, {
            method: Method,
            headers: Headers
        }).then(fetchRes => {
            res(fetchRes);
        }).catch(error => {
            rej(error);
        });
    });
}

function sendGETRequest(URL = "", Headers = {}) {
    return new Promise((res, rej) => {
        fetch(URL, {
            method: "GET",
            headers: Headers
        }).then(fetchRes => fetchRes.body).then(resReadableStream => {
            const reader = resReadableStream.getReader();

            return new ReadableStream({
                start(Controller) {
                    function push() {
                        reader.read().then(({ done, value }) => {
                            if (done) {
                                Controller.close();
                                return;
                            }

                            Controller.enqueue(value);
                            push();
                        });
                    }

                    push();
                }
            });
        }).then(stream => {
            return new Response(stream, { headers: { "Content-Type": "text/json" } }).text();
        }).then(result => {
            res(JSON.parse(result))
        }).catch(error => {
            rej(error);
        });
    });
}

function startZoomImageScroll(elementID) {
    var scale = 1;
    var minScale = .8;
    var maxScale = 10;
    var panning = false;
    var pointX = 0;
    var pointY = 0;
    var start = { x: 0, y: 0 };
    var zoom = document.getElementById(elementID);

    function setTransform() {
        zoom.style.transform = "translate(" + pointX + "px, " + pointY + "px) scale(" + scale + ")";
    }

    zoom.onmousedown = event => {
        event.preventDefault();

        start = { x: event.clientX - pointX, y: event.clientY - pointY };
        panning = true;
    }

    zoom.onmouseup = event => {
        panning = false;
    }

    zoom.onmousemove = event => {
        event.preventDefault();
        if (!panning) return;

        pointX = (event.clientX - start.x);
        pointY = (event.clientY - start.y);

        setTransform();
    }

    zoom.onwheel = event => {
        event.preventDefault();

        var xs = (event.clientX - pointX) / scale;
        var ys = (event.clientY - pointY) / scale;
        var delta = (event.wheelDelta ? event.wheelDelta : -event.deltaY);

        delta > 0 ? scale *= 1.2 : scale /= 1.2;

        if (scale < minScale) scale = minScale;
        else if (scale > maxScale) scale = maxScale;
        else {
            pointX = event.clientX - xs * scale;
            pointY = event.clientY - ys * scale;
        }

        setTransform();
    }
}

//* Display functions
function openPopUpWindow(popUpWindowID, displayStyle) {
    var popUpWindow = document.getElementById(popUpWindowID);
    if (popUpWindow) {
        popUpWindow.style.opacity = "0";
        popUpWindow.style.animation = "fadeIn .15s ease-out forwards";
        popUpWindow.style.display = displayStyle == undefined ? "grid" : displayStyle;
    }
}

function closePopUpWindow(popUpWindowID) {
    var popUpWindow = document.getElementById(popUpWindowID);
    if (popUpWindow) {
        popUpWindow.style.opacity = "1";
        popUpWindow.style.animation = "fadeOut .1s ease-out forwards";

        window.setTimeout(() => popUpWindow.style.display = "none", .1 * 1000);
    }
}

